package cl.femase.gestionweb.vo;

import java.io.Serializable;

/**
 *
 * @author Alexander
 */
public class EstadoVO implements Serializable{

    private static final long serialVersionUID = 2589692295698776220L;

    private final String id;
    private final String label;

    public EstadoVO(String id, String label) {
        this.id = id;
        this.label = label;
    }

    public String getId() {
        return id;
    }

    public String getLabel() {
        return label;
    }

    
    
}
